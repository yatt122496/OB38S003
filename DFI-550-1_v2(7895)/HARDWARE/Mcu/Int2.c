void Int2Init(void)
{
  //-----------------------------
  //   	  bit7   bit6    bit5     bit4	  bit3    bit2	  bit1   bit0
  //EXF0    --     --      --       --    IT2.1   IT2.0     --    IE2
  //-----------------------------
//   EXF0 = Bin(00000100);
//   IntSet(INT2,0,1);
//   //-----------------------------
//   P1    |= Bin(00000100);
//   P1M0  &=~Bin(00000100);
	P30CFG = GPIO_MUX_GPIO;
	P3TRIS &= ~(1 << 0);
	P30EICFG = 0x02;
	P3EXTIE |= 1;
	IRQ_SET_PRIORITY(IRQ_P3,IRQ_PRIORITY_HIGH);
}

void Int2Isr(void)  interrupt P3EI_VECTOR
{
	if (P3EXTIF & 0x01) {
		P3EXTIF &= 0xfe;
		IrInt();
	}
	if (P3EXTIF & 0x02)
	{
		P3EXTIF = 0;
		FlaZoOn();
	}
}
#endif